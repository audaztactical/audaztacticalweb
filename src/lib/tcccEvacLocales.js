/** @param {TcccEvacLocale} [lang] */
export function getEvacLocale(lang = 'tr') {
  return TCCC_EVAC_LOCALES[lang] ?? TCCC_EVAC_LOCALES.tr
}

/**
 * @param {string[]} ids
 * @param {Record<string, string>} labelMap
 */
export function evacOptionsFromMap(ids, labelMap) {
  return ids.map((id) => ({ id, label: labelMap[id] ?? id }))
}
